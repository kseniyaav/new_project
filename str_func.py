def make_capital_letters():
    '''
    Функция принимает на вход строку и делает все буквы зашлавными
    '''
    s = input("Введите текст ")
    print(s.upper())

make_capital_letters()


def make_first_letter_capital():
    '''
    Функция принимает на вход строку и делает заглавными первые буквы каждого слова в строке
    '''
    s = input("Введите текст ")
    print(s.title())

make_first_letter_capital()

